#include "libft.h"

int		ft_atoi(const char *str)
{
	size_t	i;
	int		res;
	int		neg;

	i = 0;
	neg = 0;
	res = 0;
	while (str[i] == ' ' || str[i] == '\n' || str[i] == '\t' ||
	str[i] == '\v' || str[i] == '\f' || str[i] == '\r')
		i++;
	if (str[i] == '-')
		neg = 1;
	if (str[i] == '-' || str[i] == '+')
		i++;
	while (str[i] >= '0' && str[i] <= '9')
	{
		res = res * 10 + str[i] - '0';
		i++;
	}
	if (neg != 0)
		return (-res);
	else
		return (res);
}
